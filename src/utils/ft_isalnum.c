#include "../../includes/minishell.h"

t_bool	ft_isalnum(int str)
{
	if ((str >= '0' && str <= '9')
		|| (str >= 'A' && str <= 'Z')
		|| (str >= 'a' && str <= 'z'))
		return (TRUE_E);
	return (FALSE_E);
}
